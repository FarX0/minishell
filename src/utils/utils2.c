#include "minishell.h"

int	skip_quotes(char *str, int i)
{
	if (str[i] == 39)
	{
		i++;
		while (str[i] != 39 && str[i] != '\0')
			i++;
	}
	else if (str[i] == 34)
	{
		i++;
		while (str[i] != 34 && str[i] != '\0')
			i++;
	}
	if (str[i] == '\0')
		return (-1);
	i++;
	return (i);
}
